    public void cargarUsuario(){
        Conexion cn = new Conexion();
        Connection con = cn.conectar();
        String[] datos = new String[8];
        String sql = "SELECT nom_usu,apellidos,dni,telefono,direccion,email,pass_usu,perfil FROM tusuario";
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {                
                datos[0]= rs.getString("nom_usu");
               datos[1]= rs.getString("apellidos");
               datos[2]= rs.getString("dni");
               datos[3]= rs.getString("telefono");
               datos[4]= rs.getString("direccion");
               datos[5]= rs.getString("email");
               datos[6]= rs.getString("pass_usu");
               datos[7]= rs.getString("perfil");
               model.addRow(datos);                        
            }            
            tbl_datos.setModel(model);                
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    

